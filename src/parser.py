"""
This script reads the JSON report generated by Google OSV-Scanner.

It loads the JSON data and returns it to the main application for further processing.

Parameters:
        file_path (str): Path to the JSON results file.

Returns the parsed JSON data if successful, otherwise None.
"""

import json

def load_results(file_path):

    try:
        with open(file_path, "r") as file:
            data = json.load(file)

        return data

    except Exception as error:
        print(f"An unexpected error occurred: {error}")
        return None